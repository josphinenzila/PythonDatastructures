""" Adding/Concatenating - combining two sequence of the same type """

#string
x = "horse" + "shoe"
print(x)

#list
y = ["pig", "cow"] + ["horse"]
print(y)

#tuple
z = ("kevin", "niklas", "jenny") + ("craig",)
print(z)

""" Multiplying """

#string
x = "bug" * 3
print(x)

#list
y = [8, 5] * 3
print(y)

#tuple
z = (2, 4) * 3
print(z)